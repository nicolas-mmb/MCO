CREATE DATABASE MCO
SET DATEFORMAT dmy;
/* CREATE TABLE - Login */
USE MCO
CREATE TABLE tb_LOGIN(
	CPFX			VARCHAR(12)			NOT NULL ,
	IsADM			BIT					NOT NULL,
	IsAttendant		BIT					NOT NULL,
	Names			VARCHAR(128)		NOT NULL,
	BiologicalSex	BIT					NOT NULL,
	PASSWORDX		VARCHAR(260)		NOT NULL ,
)
GO

/* PRODUTOS */
USE MCO
CREATE TABLE tb_PRODUTO(
	PDT_ID                 INT PRIMARY KEY IDENTITY (1,1),
	PDT_NOME_PRODUTO       VARCHAR (256)          NOT NULL, 
	PDT_VALOR_PRODUTO      MONEY                 NOT NULL,
	PDT_VALIDADE_PRODUTO   SMALLDATETIME         NOT NULL,
	PDT_QUANTIDADE_PRODUTO INT                   NOT NULL,
	PDT_USER_CHANGE        VARCHAR (12),
	PDT_IMAGEM_PRODUTO     IMAGE
)
GO


/* VENDAS */
USE MCO
CREATE TABLE tb_VENDAS(
	VND_ID           INT PRIMARY KEY IDENTITY (1,1)          ,
	VND_USER_CHANGE  VARCHAR                  (32)   NOT NULL, 
	VND_VALOR_TOTAL  DECIMAL                  (6,2)  NOT NULL,
	VND_DATA_VENDA   DATE                            NOT NULL,
)
GO


